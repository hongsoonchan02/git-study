package kr.co.training;

public class Ex24_4_MonsterAbility {
	private int damage = 5; 
	private double hp = 100;
	
	// setter
	public void setDamage(int damage) {
		this.damage = damage;
	}
	public void setHp(double hp) {
		this.hp = hp;
	}
	
	// getter
	public int getDamage() {
		return damage;
	}
	public double getHp() {
		return hp;
	}
}
